function getCategory(categoryId) {
  return CATEGORIES.find((category) => category.id === categoryId);
}

function getCategoryColor(categoryId) {
  const category = getCategory(categoryId);
  return category ? `var(${category.cssVar})` : "var(--ink)";
}

function getImage(imageId) {
  return IMAGE_SOURCES[imageId] || IMAGE_SOURCES.skyline;
}

function getSortedNews(items = NEWS) {
  return [...items].sort((a, b) => new Date(b.date) - new Date(a.date));
}

function formatDate(dateValue) {
  return new Intl.DateTimeFormat("ru-RU", {
    day: "numeric",
    month: "long",
    year: "numeric",
  }).format(new Date(`${dateValue}T12:00:00`));
}

function articleUrl(newsItem) {
  return `article.html?id=${encodeURIComponent(newsItem.id)}`;
}
